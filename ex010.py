nota1 = float(input('Digite a primeira nota do aluno: '))
nota2 = float(input('Digite a segundo nota do aluno: '))
media = ((nota1 + nota2)/2)
print('A média entre {:.1f} e {:.1f} é igual à {:.1f}.'.format(nota1, nota2, media))
