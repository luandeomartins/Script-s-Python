celsius = float(input('Digite a temperatura em °C: '))
fahrenheit = celsius * 9 / 5 + 32
print('A temperatura de {:.1f}°C corresponde a {:.1f}°F!'.format(celsius, fahrenheit))
# Programa que converte celsius em fahrenheit.

# Detalhe: a expressão pode ser feita sem parênteses pois a ordem de precedência da expressão
# é exatamente a ordem que os operadores aparecem.
