numero = int(input('Digite um número: '))
dobro = numero * 2
triplo = numero * 3
raizquadrada = numero ** (1 / 2)
print('O dobro do número digitado é: {}.'.format(dobro))
print('O triplo do número digitado é: {}'.format(triplo))
print('A raiz quadrada do número digitado é: {:.2f}'.format(raizquadrada))
# Programa que calcula o dobro, o triplo e a raiz quadrada do número digitado.
# {:.2f} -> duas casas decimais flutuantes.
