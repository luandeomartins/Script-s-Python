metros = float(input('Digite um valor em metros: '))
quilometros = metros/1000
hectometros = metros/100
decametros = metros/10
decimetros = metros/0.1
centimetros = metros*100
milimetros = metros*1000
print('O valor digitado em quilômetros é {:.1f}.'.format(quilometros))
print('O valor digitado em hectômetros é {:.1f}.'.format(hectometros))
print('O valor digitado em decâmetros é {:.1f}.'.format(decametros))
print('O valor digitado em decímetros é {:.1f}.'.format(decimetros))
print('O valor digitado em centímetros é {:.1f}.'.format(centimetros))
print('O valor digitado em milimetros é {:.1f}.'.format(milimetros))
